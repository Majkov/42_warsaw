char	*ft_strchr(const char *s, int c)
{
	while (*s != '\0')
	{
		if (*s == c % 256)
		{
			return ((char *)s);
		}
		++s;
	}
	if (*s == c % 256)
		return ((char *)s);
	else
		return (0);
}
