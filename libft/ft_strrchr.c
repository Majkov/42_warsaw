#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	i;

	i = 0;
	i = ft_strlen(s);
	while (i >= 0 && s[i] != c % 256)
	{
		i--;
		if (i < 0)
			return (NULL);
	}
	return ((char *)&s[i]);
}
